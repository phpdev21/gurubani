<script>
    jQuery('.article_slider_wrapper').slick({
        dots: false,
        arrows: true,
        infinite : false,
        prevArrow : "<button class='slick-prev slick-arrow' aria-label='Previous' type='button'><i class='fa-solid fa-chevron-left'></i></button>",
        nextArrow : "<button class='slick-next slick-arrow' aria-label='Next' type='button'><i class='fa-solid fa-chevron-right'></i></button>",
    })
    var get_slider_height = jQuery('.inner_article .slick-list').height();
    var totalpageno = jQuery('.inner_article .slick-track').children().length;
    jQuery('.total_pageno').text(totalpageno);
    jQuery('.gotoslide').keypress(function(e) {
        var slideno = jQuery(this).val();
        if(slideno >= 1 && slideno <= totalpageno)
        {
            if(e.which == 13)
            {
                e.preventDefault();
                jQuery('.article_slider_wrapper').slick('slickGoTo', slideno - 1);
            }
        }
    });
    jQuery('.article_slide').css('min-height' , get_slider_height+'px');

</script>
